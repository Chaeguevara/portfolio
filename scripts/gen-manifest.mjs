// Generate public/patterns/FOLDABILITY.md — a committed, git-tracked reference of
// every catalog pattern's golden-standard properties. Deterministic (the solver +
// analyses are), so it only changes when patterns change → a real regression baseline.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { installDOM } from './domshim.mjs';

installDOM();
const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const { PATTERN_CASES } = await import(path.join(root, 'src/app/fold/patternCatalog.ts'));
const { importSVG } = await import(path.join(root, 'src/app/fold/svgImport.ts'));
const { importFOLD } = await import(path.join(root, 'src/app/fold/foldFormat.ts'));
const { applyCuts } = await import(path.join(root, 'src/app/fold/kirigami.ts'));
const { createSolver, DEFAULT_PARAMS } = await import(path.join(root, 'src/app/fold/solver.ts'));
const { analyzeFlatFoldability } = await import(path.join(root, 'src/app/fold/foldability.ts'));
const { selfIntersectionCount } = await import(path.join(root, 'src/app/fold/collision.ts'));

const rows = [];
let folds = 0, flat = 0, phys = 0;
for (const g of PATTERN_CASES) {
  for (const it of g.items) {
    const text = fs.readFileSync(path.join(root, 'public/patterns', it.file), 'utf8');
    let ok = true, flatFold = false, physical = '—';
    try {
      let p = /\.fold$/i.test(it.file) ? importFOLD(text) : importSVG(text);
      p = applyCuts(p);
      const s = createSolver({ vertices: p.vertices, faces: p.faces, edges: p.edges }, { ...DEFAULT_PARAMS });
      s.setFoldPercent(0.85);
      s.step(300);
      for (let i = 0; i < s.positions.length; i++) if (Number.isNaN(s.positions[i])) throw new Error('NaN');
      folds++;
      flatFold = analyzeFlatFoldability(p).flatFoldable;
      if (flatFold) flat++;
      if (p.faces.length <= 2000) {
        s.setFoldPercent(0.7);
        s.step(300);
        const clean = selfIntersectionCount(s.positions, p.faces) === 0;
        physical = clean ? 'clean' : '⚠ penetrates';
        if (clean) phys++;
      } else {
        physical = 'large (skipped)';
      }
    } catch (e) {
      ok = false;
      physical = `FAIL: ${e.message}`;
    }
    rows.push(`| ${g.group} | \`${it.file}\` | ${ok ? '✓' : '✗'} | ${flatFold ? 'flat-foldable' : ok ? '3D shell' : '—'} | ${physical} |`);
  }
}

const total = rows.length;
const md = `# Pattern foldability manifest

_Auto-generated by \`node scripts/gen-manifest.mjs\`. Do not edit by hand._

Golden-standard properties of every crease/cut pattern the repo ships:

- **Folds** — imports and folds in the compliant solver without producing NaN.
- **Flat-foldable** — satisfies Maekawa (\`|M−V|=2\`) + Kawasaki (alternating angle sum) at every interior vertex, so it can fold flat; otherwise it forms a 3D shell.
- **Physical** — folds to 70% with no panel passing through another (the compliant solver has no collision, so \`⚠ penetrates\` marks patterns that need collision handling).

**Summary:** ${folds}/${total} fold cleanly · ${flat} flat-foldable · ${phys} physically clean.

| Group | Pattern | Folds | Flat-foldable | Physical |
|---|---|:-:|:-:|:-:|
${rows.join('\n')}
`;
fs.writeFileSync(path.join(root, 'public/patterns/FOLDABILITY.md'), md);
console.log(`gen:manifest wrote FOLDABILITY.md — ${folds}/${total} fold, ${flat} flat-foldable, ${phys} physically clean`);
